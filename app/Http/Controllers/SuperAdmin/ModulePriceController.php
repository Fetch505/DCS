     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $modulePrice=new ModulePrice();
        $modulePrice->module_name=$request->name;
        $modulePrice->module_dutch_name=$request->dutch;
        $modulePrice->monthly_price=$request->monthly;
        $modulePrice->yearly_price=$request->yearly;
        $modulePrice->module_discount=$request->discount;
        $modulePrice->save();
        return redirect()->back();
    }

